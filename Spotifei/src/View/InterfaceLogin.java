    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
        String senha = new String(txtSenha.getPassword());
        AcessoBanco acesso = new AcessoBanco();
        boolean loginValido = acesso.validarLogin(email, senha); 

        if (loginValido) {
            JOptionPane.showMessageDialog(this, "Login realizado com sucesso!");

            // Fechar a janela de login (InterfaceJframe)
            this.dispose(); // Isso vai fechar a janela de login

            // Abrir a janela de menu do usu치rio com o nome do usu치rio logado
            String nomeUsuario = txtEmail.getText(); // Supondo que o nome do usu치rio venha do campo email
            new InterfaceMenu(nomeUsuario).setVisible(true); // Chama o menu do usu치rio
        } else {
            JOptionPane.showMessageDialog(this, "Email ou senha incorretos.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

